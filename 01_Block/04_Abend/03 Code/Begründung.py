string = "Hai"
print(string)
"""Ein String (str) ist ein immutable (unveränderlicher) Datentyp, der Text speichert"""

tuple = ("H","a","i")
print(tuple)
"""In Python ist ein Tuple eine unveränderliche (immutable) geordnete Sammlung von Werten."""


liste = ["H","a","i"]
print(liste[0])
"""In Python ist eine Liste (list) eine veränderbare, geordnete Sammlung von Elementen."""